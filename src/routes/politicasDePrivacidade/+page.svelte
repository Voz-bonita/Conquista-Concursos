	<h1>Política de Privacidade</h1>

	<p>
		Esta Política de Privacidade ("Política") descreve como coletamos, usamos e protegemos suas
		informações pessoais quando você acessa e utiliza o site Conquista Concursos ("Site"). Ao usar o
		Site, você concorda com esta Política. Se não concordar, não utilize o Site.
	</p>
	<p></p>
	<h3>1. Coleta de Informações</h3>
	<p>
		Podemos coletar informações pessoais como nome, e-mail, informações de pagamento e outras
		informações necessárias para o uso do Site. Além disso, coletamos automaticamente informações
		sobre seu uso do Site, como histórico de navegação, respostas a questões e interações com a
		plataforma.
	</p>

	<h3>2. Uso das Informações</h3>
	<p>
		As informações coletadas podem ser usadas para: Fornecer e melhorar nossos serviços.
		Personalizar sua experiência no Site. Enviar atualizações, notificações ou ofertas promocionais.
		Processar transações e pagamentos. Analisar tendências e aprimorar nossos modelos de IA. Podemos
		utilizar as respostas dos usuários para melhorar e treinar nossos modelos de IA e para fins de
		análise interna. Ao utilizar o Site, você consente com esse uso de suas respostas.
	</p>

	<h3>3. Compartilhamento de Informações</h3>
	<p>
		Podemos compartilhar suas informações com terceiros nos seguintes casos:Provedores de Serviços:
		Usamos o Google Firebase como backend e Stripe para processar pagamentos. Eles podem ter acesso
		a certas informações para fornecer esses serviços. Estes provedores podem ter suas próprias
		políticas de privacidade, às quais você também está sujeito. Parceiros de IA: Provedores de
		modelos de IA também podem ter acesso a dados de respostas dos usuários para fins de melhoria e
		treinamento de modelos. Esses provedores têm suas próprias políticas de privacidade e termos de
		uso. Obrigação Legal: Podemos divulgar informações se acreditarmos que tal ação é necessária
		para cumprir uma obrigação legal ou proteger nossos direitos, propriedade ou segurança.
	</p>
	<h3>4. Alterações nos Termos dos Provedores</h3>
	<p>
		Ao usar o Site, você está sujeito aos termos e políticas dos provedores terceirizados, como
		Firebase e Stripe. Aconselhamos revisar essas políticas periodicamente, pois estão sujeitas a
		alterações sem aviso prévio. Não somos responsáveis por quaisquer alterações nas políticas
		desses provedores ou pelo uso de suas informações por terceiros.
	</p>

	<h3>5. Proteção das Informações</h3>
	<p>
		Tomamos medidas para proteger suas informações contra acesso não autorizado, uso ou divulgação.
		No entanto, não podemos garantir segurança absoluta. Ao usar o Site, você reconhece e aceita os
		riscos inerentes à transmissão eletrônica de dados.
	</p>

	<h3>6. Seus Direitos</h3>
	<p>
		Você pode solicitar acesso, correção ou exclusão de suas informações pessoais a qualquer
		momento, conforme permitido por lei. Para fazer uma solicitação, entre em contato conosco
		através do e-mail suporte@conquista-concursos.com ou do formulário de contato no Site.
	</p>

	<h3>7. Alterações nesta Política</h3>
	<p>
		Podemos modificar esta Política a qualquer momento. Notificações sobre alterações serão
		publicadas no Site ou enviadas por e-mail para usuários registrados. O uso contínuo do Site após
		a publicação de alterações constitui aceitação das mudanças.
	</p>

	<h3>8. Contato</h3>
	<p>
		Se tiver dúvidas sobre esta Política ou sobre o tratamento de suas informações pessoais, entre
		em contato conosco pelo e-mail suporte@conquista-concursos.com ou pelo formulário de contato no
		Site.
	</p>
